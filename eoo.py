def evenOrOdd(number):
    numberS = str(number) 
    digit = numberS[-1:]
    if digit == '1':
        print('odd')
    if digit == '2':
        print('even')
    if digit == '3':
        print('odd')
    if digit == '4':
        print('even')
    if digit == '5':
        print('odd')
    if digit == '6':
        print('even')
    if digit == '7':
        print('odd')
    if digit == '8':
        print('even')
    if digit == '9':
        print('odd')
    if digit == '0':
        print('even')


evenOrOdd(-120000000001)
